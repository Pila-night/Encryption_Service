std::vector<unsigned char> xor_64(std::vector<unsigned char> a, std::vector<unsigned char> b) {
    std::vector<unsigned char> result(64);
    for (int i = 0; i < 64; ++i) result[i] = a[i] ^ b[i];
    return result;
}

