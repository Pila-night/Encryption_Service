std::vector<unsigned char> L_transform(std::vector<unsigned char> data) {
    uint64_t data8byte[8];
    for (int i = 0; i < 8; ++i) {
        data8byte[i] = 0;
        for (int j = 0; j < 8; ++j)
            data8byte[i] |= ((uint64_t)data[i * 8 + j]) << (j * 8);
    }
    uint64_t res[8] = {0};
    for (int i = 0; i < 8; ++i) {
        uint64_t val = data8byte[i];
        for (int j = 0; j < 64; ++j) {
            if (val & 1) {
                for (int k = 0; k < 8; ++k)
                    res[k] ^= A[j * 8 + k];
            }
            val >>= 1;
        }
    }
    std::vector<unsigned char> output(64);
    for (int i = 0; i < 8; ++i)
        for (int j = 0; j < 8; ++j)
            output[i * 8 + j] = (res[i] >> (j * 8)) & 0xFF;
    return output;
}

std::vector<unsigned char> LPS(std::vector<unsigned char> data) {
    return L_transform(P_transform(S_transform(data)));
}

std::vector<unsigned char> g_N(const std::vector<unsigned char>& h,
                               const std::vector<unsigned char>& m,
                               const std::vector<unsigned char>& N) {
    auto K = LPS(xor_64(h, N));
    auto result = LPS(xor_64(K, m));
    return xor_64(result, m);
}

void add_512(std::vector<unsigned char>& N) {
    uint16_t carry = 512;
    for (int i = 0; i < 64 && carry > 0; ++i) {
        uint16_t sum = N[i] + (carry & 0xFF);
        N[i] = sum & 0xFF;
        carry = (carry >> 8) + (sum >> 8);
    }
}

void add_block(std::vector<unsigned char>& sigma, const std::vector<unsigned char>& block) {
    uint16_t carry = 0;
    for (int i = 0; i < 64; ++i) {
        uint16_t sum = sigma[i] + block[i] + carry;
        sigma[i] = sum & 0xFF;
        carry = sum >> 8;
    }
}

