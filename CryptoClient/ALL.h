// Сложение двух точек P1=(x1,y1) + P2=(x2,y2) => R=(rx,ry)
void point_add(mpz_t rx, mpz_t ry,
               const mpz_t x1, const mpz_t y1,
               const mpz_t x2, const mpz_t y2) {
    if (mpz_cmp(x1, x2) == 0 && mpz_cmp(y1, y2) == 0) {
        // Удвоение точки
        mpz_t lambda, num, den, tmp;
        mpz_inits(lambda, num, den, tmp, nullptr);
        // num = 3*x1^2 + a
        mpz_mul(num, x1, x1);
        mpz_mul_ui(num, num, 3);
        mpz_add(num, num, a);
        mpz_mod(num, num, p);
        // den = 2*y1
        mpz_mul_ui(den, y1, 2);
        mpz_mod(den, den, p);
        // lambda = num / den mod p
        mpz_invert(tmp, den, p);
        mpz_mul(lambda, num, tmp);
        mpz_mod(lambda, lambda, p);

        // rx = lambda^2 - 2*x1
        mpz_mul(tmp, lambda, lambda);
        mpz_sub(rx, tmp, x1);
        mpz_sub(rx, rx, x1);
        mpz_mod(rx, rx, p);

        // ry = lambda*(x1 - rx) - y1
        mpz_sub(tmp, x1, rx);
        mpz_mul(ry, lambda, tmp);
        mpz_sub(ry, ry, y1);
        mpz_mod(ry, ry, p);

        mpz_clears(lambda, num, den, tmp, nullptr);
    } else {
        // Сложение различных точек
        mpz_t lambda, num, den, tmp;
        mpz_inits(lambda, num, den, tmp, nullptr);
        // num = y2 - y1
        mpz_sub(num, y2, y1);
        // den = x2 - x1
        mpz_sub(den, x2, x1);
        mpz_mod(den, den, p);
        // lambda = num / den mod p
        mpz_invert(tmp, den, p);
        mpz_mul(lambda, num, tmp);
        mpz_mod(lambda, lambda, p);

        // rx = lambda^2 - x1 - x2
        mpz_mul(tmp, lambda, lambda);
        mpz_sub(rx, tmp, x1);
        mpz_sub(rx, rx, x2);
        mpz_mod(rx, rx, p);

        // ry = lambda*(x1 - rx) - y1
        mpz_sub(tmp, x1, rx);
        mpz_mul(ry, lambda, tmp);
        mpz_sub(ry, ry, y1);
        mpz_mod(ry, ry, p);

        mpz_clears(lambda, num, den, tmp, nullptr);
    }
}

// Скалярное умножение: R = k * P, где P=(px,py)
void point_mul(mpz_t rx, mpz_t ry, const mpz_t k, const mpz_t px, const mpz_t py) {
    mpz_set_ui(rx, 0);
    mpz_set_ui(ry, 0);
    if (mpz_cmp_ui(k, 0) == 0) return;

    mpz_t kk, tx, ty, acc_x, acc_y;
    mpz_inits(kk, tx, ty, acc_x, acc_y, nullptr);
    mpz_set(kk, k);
    mpz_set(tx, px);
    mpz_set(ty, py);

    while (mpz_cmp_ui(kk, 0) > 0) {
        if (mpz_odd_p(kk)) {
            if (mpz_cmp_ui(rx, 0) == 0 && mpz_cmp_ui(ry, 0) == 0) {
                mpz_set(rx, tx);
                mpz_set(ry, ty);
            } else {
                point_add(acc_x, acc_y, rx, ry, tx, ty);
                mpz_set(rx, acc_x);
                mpz_set(ry, acc_y);
            }
        }
        // удвоение (tx,ty)
        point_add(acc_x, acc_y, tx, ty, tx, ty);
        mpz_set(tx, acc_x);
        mpz_set(ty, acc_y);
        mpz_fdiv_q_2exp(kk, kk, 1);
    }
    mpz_clears(kk, tx, ty, acc_x, acc_y, nullptr);
}

// Вспомогательная функция преобразования байта в hex (нужна для старой реализации хеша, оставлена)
std::string to_hex(unsigned char byte) {
    const char hex_chars[] = "0123456789abcdef";
    std::string s;
    s += hex_chars[byte >> 4];
    s += hex_chars[byte & 0x0F];
    return s;
}

