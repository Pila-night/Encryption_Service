// Преобразование mpz_t в hex-строку длиной 64 символа (256 бит)
std::string mpz_to_hex_64(const mpz_t num) {
    char* hex = mpz_get_str(nullptr, 16, num);
    std::string result(hex);
    free(hex);
    if (result.length() < 64) {
        result.insert(0, 64 - result.length(), '0');
    }
    return result;
}

// Преобразование hex-строки в mpz_t
void hex_to_mpz(mpz_t num, const std::string& hex) {
    mpz_set_str(num, hex.c_str(), 16);
}

