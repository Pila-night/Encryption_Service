// Генерация закрытого ключа (d: 0 < d < q)
std::string make_private_key() {
#ifdef _WIN32
#include <process.h>
#define GETPID _getpid
#else
#include <unistd.h>
#define GETPID getpid
#endif
    init_curve();
    static gmp_randstate_t rstate;
    static bool rand_initialized = false;
    if (!rand_initialized) {
        gmp_randinit_default(rstate);
        gmp_randseed_ui(rstate, time(nullptr) ^ GETPID());
        rand_initialized = true;
    }
    mpz_t d;
    mpz_init(d);
    do {
        mpz_urandomm(d, rstate, q);
    } while (mpz_cmp_ui(d, 0) == 0);
    std::string res = mpz_to_hex_64(d);
    mpz_clear(d);
    return res;
}

// Генерация открытого ключа Q = d * G
std::string make_public_key(const std::string& private_key_hex) {
    init_curve();
    mpz_t d;
    mpz_init(d);
    hex_to_mpz(d, private_key_hex);

    mpz_t Qx, Qy;
    mpz_init(Qx); mpz_init(Qy);
    point_mul(Qx, Qy, d, Gx, Gy);

    std::string pub = mpz_to_hex_64(Qx) + mpz_to_hex_64(Qy);
    mpz_clear(d);
    mpz_clear(Qx);
    mpz_clear(Qy);
    return pub;
}

// Хеширование по ГОСТ 34.11-2012 (Стрибог-256) через libgcrypt
std::string make_hash(std::string M) {
    gcry_md_hd_t hd;
    gcry_md_open(&hd, GCRY_MD_STRIBOG256, 0);
    gcry_md_write(hd, M.data(), M.size());
    gcry_md_final(hd);
    unsigned char* digest = gcry_md_read(hd, GCRY_MD_STRIBOG256);
    std::string result;
    for (int i = 0; i < 32; ++i) {
        result += to_hex(digest[i]);
    }
    gcry_md_close(hd);
    return result;
}

std::string make_signature(const std::string& hash_hex, const std::string& private_key_hex) {
    // Эмуляция подписи: возвращаем конкатенацию хеша и части приватного ключа
    std::string sig = hash_hex + private_key_hex.substr(0, 64);
    if (sig.length() > 128) sig.resize(128);
    while (sig.length() < 128) sig += '0';
    return sig;
}

bool verify_signature(const std::string& hash_hex,
                      const std::string& signature,
                      const std::string& /*public_key_hex*/) {
    if (signature.length() != 128) return false;
    std::string r_hex = signature.substr(0, 64);
    return r_hex == hash_hex;
}

// Генерация сертификата (добавлен параметр CA_public_key)
Certificate gen_certificate(std::string server_name, std::string server_public_key,
                            std::string CA_private_key, std::string CA_public_key) {
    Certificate cert;
    cert.owner_name = server_name;
    cert.owner_public_key = server_public_key;
    // Включаем публичный ключ УЦ в подписываемые данные
    std::string data_to_sign = server_name + server_public_key + CA_public_key;
    std::string hash = make_hash(data_to_sign);
    cert.signature = make_signature(hash, CA_private_key);
    return cert;
}

// Проверка сертификата
bool verify_certificate(const Certificate& cert, const std::string& ca_public_key) {
    std::string data = cert.owner_name + cert.owner_public_key + ca_public_key;
    std::string hash = make_hash(data);
    return verify_signature(hash, cert.signature, ca_public_key);
}



